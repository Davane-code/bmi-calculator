query = st.text_input("请输入要搜索的题目：")

# 在网页上显示一个按钮，标签为"搜索"
button = st.button("搜索")

# 如果用户点击了按钮，就执行搜索结果的显示
if button:
    # 定义一个变量，用于记录是否找到了答案
    found = False
    # 遍历字典中的所有题目和答案
    for question, answer in questions.items():
        # 如果输入的字符串在题目中出现，就显示题目和答案，并将变量设为True
        if query in question:
            st.write(f"题目：{question}")
            # st.write(f"答案：{answer}")
            # 用HTML标签包裹答案，并设置背景颜色为黄色
            st.markdown(f"<b>答案</b>：<p style='background-color:yellow'>{answer}</p>", unsafe_allow_html=True)
            # st.markdown(f"<p style='background-color:yellow'>答案：<b>{answer[:2]}</b>{answer[2:]}</p>", unsafe_allow_html=True)
            found = True
    # 如果遍历完字典后，变量仍然是False，就表示没有找到答案
    if not found:
        st.write("抱歉，没有找到答案。")


